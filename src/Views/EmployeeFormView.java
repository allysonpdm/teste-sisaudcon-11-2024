    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String name = fieldName.getText();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        
        Date hireDate = null;
        try {
            hireDate = dateFormat.parse(fieldHireDate.getText());
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Data de admissão inválida.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Double salary = null;
        try {
            salary = Double.parseDouble(fieldSalary.getText().replace(".", "").replace(",", "."));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Salário inválido.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean status = checkStatus.isSelected();
        response = isEditing(employee)
            ? controller.update(employee.getId(), userId, name, hireDate, salary, status)
            : controller.create(userId, name, hireDate, salary, status);
        
        employee = response.getEmployee();
        if (employee != null && employee.getId() != null) {
            JOptionPane.showMessageDialog(null, response.getMessage(), "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            dispose();
            return;
        }

        JOptionPane.showMessageDialog(null, response.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);          
        
    }//GEN-LAST:event_btnSaveActionPerformed

    private boolean isEditing(EmployeeDto employee) {
        return employee != null && employee.getId() != null;
    }
    
