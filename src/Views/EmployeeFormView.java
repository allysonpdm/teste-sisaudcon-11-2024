    private void fieldHireDateFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_fieldHireDateFocusLost

        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        dateFormat.setLenient(false); // Para evitar datas inválidas como 32/13/0000
        String text = fieldHireDate.getText();

        if (text.length() != 10) {
            fieldHireDate.setValue(null);
            return;
        }

        try {
            Date date = dateFormat.parse(text);
            fieldHireDate.setValue(dateFormat.format(date));
        } catch (ParseException e) {
            fieldHireDate.setValue(null);
        }
    }//GEN-LAST:event_fieldHireDateFocusLost

    private void fieldSalaryFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_fieldSalaryFocusLost
        String text = fieldSalary.getText();

        text = text.replace(".", "");
        text = text.replace(",", ".");

        try {
            double value = Double.parseDouble(text);

            DecimalFormatSymbols symbols = new DecimalFormatSymbols(new Locale("pt", "BR"));
            symbols.setDecimalSeparator(',');
            symbols.setGroupingSeparator('.');

            DecimalFormat df = new DecimalFormat("#,##0.00", symbols);
            fieldSalary.setText(df.format(value));
        } catch (NumberFormatException e) {
            fieldSalary.setText("");
        }
    }//GEN-LAST:event_fieldSalaryFocusLost

